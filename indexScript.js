function printDate() {
    let year = "", month = "", day = "";
    let date = document.getElementById("DateSender").value;
    for (let i = 0; i < date.length; i++) {
        if (i < 4) { year += date.charAt(i); }
        if (i > 4 && i < 7) { month += date.charAt(i); }
        if (i > 7) { day += date.charAt(i); }
    }
    let city = document.getElementById("CitySender").value;

    if (month == "01") { month = "January"; }
    else if (month == "02") { month = "February"; }
    else if (month == "03") { month = "March"; }
    else if (month == "04") { month = "April"; }
    else if (month == "05") { month = "May"; }
    else if (month == "06") { month = "June"; }
    else if (month == "07") { month = "July"; }
    else if (month == "08") { month = "August"; }
    else if (month == "09") { month = "September"; }
    else if (month == "10") { month = "October"; }
    else if (month == "11") { month = "November"; }
    else if (month == "12") { month = "December"; }

    if (day.charAt(0) == 0) { day = day.charAt(1); }

    if (day == "1" || day == "21" || day == "31") { day = day + "st"; }
    else if (day == "2" || day == "22") { day = day + "nd"; }
    else if (day == "3" || day == "23") { day = day + "rd"; }
    else { day = day + "th"; }
    //alert(city + ", " + month + " " + day + ", " + year);
    let message = city + ", " + month + " " + day + ", " + year;
    document.getElementById("Date").style.textAlign = "right";
    document.getElementById("Date").innerHTML = message;
}

